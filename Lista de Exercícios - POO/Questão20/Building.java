package Questão20;

public class Building {
    
    private int numero_pessoas;
    private boolean uso_energia_renovável;
    private int numero_lampadas;
    private boolean uso_ar_condicionado;
    
    
    public int getNumero_pessoas() {
        return numero_pessoas;
    }
    public void setNumero_pessoas(int numero_pessoas) {
        this.numero_pessoas = numero_pessoas;
    }
    public boolean isUso_energia_renovável() {
        return uso_energia_renovável;
    }
    public void setUso_energia_renovável(boolean uso_energia_renovável) {
        this.uso_energia_renovável = uso_energia_renovável;
    }
    public int getNumero_lampadas() {
        return numero_lampadas;
    }
    public void setNumero_lampadas(int numero_lampadas) {
        this.numero_lampadas = numero_lampadas;
    }
    public boolean isUso_ar_condicionado() {
        return uso_ar_condicionado;
    }
    public void setUso_ar_condicionado(boolean uso_ar_condicionado) {
        this.uso_ar_condicionado = uso_ar_condicionado;
    }

    
}
